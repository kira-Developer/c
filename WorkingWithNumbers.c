#include <stdio.h>
#include <stdlib.h>
#include <math.h>
int main () {
    int number = 3;
    printf("%f\n" , 8.9);
    printf("%f\n" , 5.1 + 8.9);
    printf("%f\n" , 5 + 4.3);
    printf("%d\n" , number);
    printf("%f\n" , pow(2,4)); // 2 ^ 4
    printf("%f\n" , sqrt(16));
    printf("%f\n" , ceil(16.7));
    printf("%f\n" , floor(16.656));

    return 0;
}